function printPositiveOrNegative(inputValue) {
  if (inputValue > 0) {
    console.log(inputValue, " is a positive number");
  } else {
    console.log(inputValue, " is a Negative number number");
  }
}

printPositiveOrNegative(-80);
printPositiveOrNegative(60);
printPositiveOrNegative(-50);
printPositiveOrNegative(-40);
printPositiveOrNegative(9);
